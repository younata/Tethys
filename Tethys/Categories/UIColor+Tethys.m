#import "UIColor+Tethys.h"

@implementation UIColor (Tethys)

+ (UIColor *)darkGreenColor
{
    return [self colorWithRed:0 green:0.75 blue:0 alpha:1.0];
}

+ (UIColor *)mediumDarkGrayColor
{
    return [self colorWithWhite:0.4 alpha:1.0];
}

+ (UIColor *)darkVioletColor
{
    return [self colorWithRed:0.25 green:0 blue:0.5 alpha:1.0];
}

@end
